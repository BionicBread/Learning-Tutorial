let x = 10;
// = is the assignment operator 

x++; //these are the same
x = x + 1; // adding 1 to x

// in the case of larger increment values
x = x + 5; //increment of 5 steps
x += 5; //this is the assignment operator to increments of 5

x = x * 3;
x *= 3; //any mathematic operator can be used with the assignment operator