// Ternary aka Conditional operator
// If a customer has more than 100 points
// they are a 'gold' customer, otherwise,
// they are a 'silver' customer

let points = 110;
let type = points > 100 ? 'gold' : 'silver'; // type variable will be a boolean, the ? operator will then check the condition: true is gold and false is silver
console.log(type);   // the returned value for type is one of the respective string above  

// easy way to write an if statement