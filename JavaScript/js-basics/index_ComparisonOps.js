// Use to compare variables with another
let x = 1;

// Relational operators
console.log(x > 0); //is x greater than 0 (returns a boolean) true
console.log(x >= 1); // is x greater than or equal to 1  true
console.log(x < 1); // is x less than 1 false (x = 1 therefore not less than 1)
console.log(x <= 1); // is x less than or equal to 1 true

// Equality
console.log(x === 1); // is x is equal to 1 (or a value)
console.log(x !== 1); // is x not equal to 1 (or a value)